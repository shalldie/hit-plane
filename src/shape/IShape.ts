interface IShape {

    /**
     * x 坐标
     * 
     * @type {number}@memberof IShape
     */
    x: number,

    /**
     * y 坐标
     * 
     * @type {number}@memberof IShape
     */
    y: number,

    /**
     * 宽度
     * 
     * @type {number}@memberof IShape
     */
    width: number,

    /**
     * 高度
     * 
     * @type {number}@memberof IShape
     */
    height: number,

    /**
     * 比例
     * 
     * @type {number}@memberof IShape
     */
    scale: number
};

export default IShape;